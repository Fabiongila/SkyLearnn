def replace_trans_tags(text: str) -> str:
    for eng, pt in TRANSLATIONS.items():
        text = text.replace("{% trans '" + eng + "' %}", pt)
        text = text.replace('{% trans "' + eng + '" %}', pt)
        text = text.replace("gettext('" + eng + "')", "'" + pt + "'")
        text = text.replace('gettext("' + eng + '")', '"' + pt + '"')
    return text


def main():
    root = Path(__file__).resolve().parent.parent / 'templates'
    for path in sorted(root.rglob('*.html')):
        text = path.read_text(encoding='utf-8')
        new_text = replace_trans_tags(text)
        new_text = new_text.replace('lang="en"', 'lang="pt"')
        new_text = new_text.replace('lang="en-US"', 'lang="pt"')
        if new_text != text:
            path.write_text(new_text, encoding='utf-8')
            print(f'Updated {path}')


if __name__ == '__main__':
    main()
